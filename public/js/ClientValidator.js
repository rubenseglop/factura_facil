/* Validar NIF */ 
function validateNifFormat(nif){
    var letras = ['T','R','W','A','G','M','Y','F','P','D','X','B','N','J','Z','S','Q','V','H','L','C','K','E'];
    if(!(/^\d{8}-?([A-Z]|[a-z])$/.test(nif.value))){
        nif.focus();
        // alert( "Error: formato incorrecto para un dni");
        return false;
    }

    var letra = nif.value.charAt(8);
    if(letra == "-"){
        letra = nif.value.charAt(9);
    }
    
    if(letra.toUpperCase() != letras[(nif.value.substring(0, 8))%23]){   
        nif.focus();
        alert("Error: formato incorrecto para un dni (La letra no coincide con el número)");
        return false;
    }

    return true;
}